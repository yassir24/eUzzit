  async updateTransactionPin() {
    const transaction_pin = await this.pinService.updatePin("Create a new pin");
    if (transaction_pin != null) {
      const transaction_pin_confirmation = await this.pinService.updatePin(
        "Retype pin"
      );
      if (transaction_pin_confirmation != null) {
        this.auth
          .updateTransPin({ transaction_pin, transaction_pin_confirmation })
          .subscribe(
            async (res) => {
              if (res.status == "success") {
                const alert = await this.alertController.create({
                  header: "Success",
                  message: "Transaction pin successfully set",
                });

                await alert.present();
              }
            },
            async (error) => {
              const alert = await this.alertController.create({
                header: "Error",
                message: "Could not set pin",
              });

              await alert.present();
            }
          );
      }
    }
  }

